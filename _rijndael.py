def _gmul(a, b):
    p = 0
    for _ in range(8):
        if b & 1: p ^= a
        a = ((a << 1) ^ 0x1b if a & 0x80 else a << 1) & 0xff
        b >>= 1
    return p


def _expand_key(key: bytes):
    Nk = len(key) // 4
    Nr = Nk + 6
    W = [int.from_bytes(key[i*4:i*4+4], 'big') for i in range(Nk)]
    rc, x = [0] * (Nr * Nk + 10), 1
    for i in range(1, len(rc)):
        rc[i] = x << 24
        x = ((x << 1) ^ 0x1b & 0xff) if x & 0x80 else (x << 1) & 0xff
    sw = lambda w: sum(_S[(w >> s) & 0xff] << s for s in (24, 16, 8, 0))
    for i in range(Nk, 4 * (Nr + 1)):
        t = W[i - 1]
        if i % Nk == 0:               t = sw(((t << 8) | (t >> 24)) & 0xffffffff) ^ rc[i // Nk]
        elif Nk > 6 and i % Nk == 4:  t = sw(t)
        W.append(W[i - Nk] ^ t)
    return W, Nr


def _ark(st, W, rnd):
    return [[st[r][c] ^ ((W[rnd*4+c] >> (24-8*r)) & 0xff) for c in range(4)] for r in range(4)]

def _sub(st, box):
    return [[box[st[r][c]] for c in range(4)] for r in range(4)]

def _shift(st, inv=False):
    return [st[r][-r:] + st[r][:-r] if (inv and r) else st[r][r:] + st[r][:r] if r else st[r] for r in range(4)]

def _mix(st, inv=False):
    m = [(14,11,13,9),(9,14,11,13),(13,9,14,11),(11,13,9,14)] if inv else [(2,3,1,1),(1,2,3,1),(1,1,2,3),(3,1,1,2)]
    return [[_gmul(m[r][0],st[0][c])^_gmul(m[r][1],st[1][c])^_gmul(m[r][2],st[2][c])^_gmul(m[r][3],st[3][c])
             for c in range(4)] for r in range(4)]

def _encrypt_block(block: bytes, W, Nr) -> bytes:
    st = [[block[r + 4*c] for c in range(4)] for r in range(4)]
    st = _ark(st, W, 0)
    for rnd in range(1, Nr + 1):
        st = _sub(st, _S)
        st = _shift(st)
        if rnd < Nr: st = _mix(st)
        st = _ark(st, W, rnd)
    return bytes(st[r][c] for c in range(4) for r in range(4))

def _decrypt_block(block: bytes, W, Nr) -> bytes:
    st = [[block[r + 4*c] for c in range(4)] for r in range(4)]
    st = _ark(st, W, Nr)
    for rnd in range(Nr - 1, -1, -1):
        st = _shift(st, inv=True)
        st = _sub(st, _INV_S)
        st = _ark(st, W, rnd)
        if rnd > 0: st = _mix(st, inv=True)
    return bytes(st[r][c] for c in range(4) for r in range(4))

def _pkcs7_pad(data: bytes) -> bytes:
    p = 16 - len(data) % 16
    return data + bytes([p] * p)

def _pkcs7_unpad(data: bytes) -> bytes:
    return data[:-data[-1]]


# ── Public standalone functions ────────────────────────────────────────────────

def rijndael_ecb_encrypt(plaintext: str | bytes, key: bytes) -> str:
    """
    Encrypt plaintext using Rijndael-ECB with PKCS7 padding.
    Supports any key size that is a multiple of 4 bytes (16, 24, 32, 64 ...).
    Returns base64-encoded ciphertext — identical to CryptoJS AES.encrypt output.

    Args:
        plaintext: string or bytes to encrypt
        key:       raw key bytes (any multiple-of-4 length)

    Returns:
        base64 encoded ciphertext string
    """
    if isinstance(plaintext, str):
        plaintext = plaintext.encode()
    W, Nr = _expand_key(key)
    data = _pkcs7_pad(plaintext)
    ct = b''.join(_encrypt_block(data[i:i+16], W, Nr) for i in range(0, len(data), 16))
    return base64.b64encode(ct).decode()


def rijndael_ecb_decrypt(ciphertext: str | bytes, key: bytes) -> str:
    """
    Decrypt base64 Rijndael-ECB ciphertext (PKCS7 padded).
    Supports any key size that is a multiple of 4 bytes (16, 24, 32, 64 ...).
    Returns the original plaintext string — identical to CryptoJS AES.decrypt output.

    Args:
        ciphertext: base64 encoded ciphertext (string or bytes)
        key:        raw key bytes (any multiple-of-4 length)

    Returns:
        decrypted plaintext string
    """
    data = base64.b64decode(ciphertext)
    W, Nr = _expand_key(key)
    pt = b''.join(_decrypt_block(data[i:i+16], W, Nr) for i in range(0, len(data), 16))
    return _pkcs7_unpad(pt).decode()


# ── pycryptodome-compatible cipher object ──────────────────────────────────────

class _RijndaelECB:
    """
    Pycryptodome-compatible cipher object for non-standard key sizes.
    Matches the interface of pycryptodome's ECB mode object.
    """
    block_size = 16

    def __init__(self, key: bytes):
        self._W, self._Nr = _expand_key(key)

    def encrypt(self, data: bytes) -> bytes:
        if len(data) % 16 != 0:
            raise ValueError("Data must be padded to block boundary before encrypting")
        return b''.join(_encrypt_block(data[i:i+16], self._W, self._Nr) for i in range(0, len(data), 16))

    def decrypt(self, data: bytes) -> bytes:
        if len(data) % 16 != 0:
            raise ValueError("Data length must be a multiple of block size")
        return b''.join(_decrypt_block(data[i:i+16], self._W, self._Nr) for i in range(0, len(data), 16))
